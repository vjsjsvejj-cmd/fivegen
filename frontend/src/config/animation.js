// 动画效果配置
// 修改 ANIMATION_MODE 的值即可切换动画效果
// 可选值: 'none', 'pop-pulse', 'streamer-bounce', 'elegant-float', 'neon-border'

export const ANIMATION_MODE = 'streamer-bounce'

// ==================== 动画模式说明 ====================
//
// none - 无动画效果
//
// pop-pulse - 弹出脉冲+渐变边框（方案1）
//   - 效果：卡片弹出放大 + 渐变霓虹边框循环
//   - 特点：炫酷、明显、有科技感
//
// streamer-bounce - 飘带闪烁+跳动（方案2）
//   - 效果：轻微跳动 + 边框色循环 + 四角✨装饰
//   - 特点：活泼、可爱、有喜庆感
//
// elegant-float - 优雅上浮（方案3）
//   - 效果：从下方上浮 + 微妙光晕
//   - 特点：低调、优雅、高级感
//
// neon-border - 霓虹跑马灯（方案4）
//   - 效果：弹出 + 彩色跑马灯边框
//   - 特点：复古、赛博朋克风
//
// ======================================================
//
// 使用方法：
// 1. 修改上面 ANIMATION_MODE 的值
// 2. 刷新页面即可看到效果
// 3. 新生成的图片/视频会有动画效果

